package 변수;

public class 변수확인 {

	public static void main(String[] args) {
		//정수
		byte age = 100; //1
		short weight = 200; //2
		int money = 100000; //4
		long space = 1000000000; //8바이트
		
		//실수 - float, double
		float height = 185.3F; //4 //디폴트값이 더블, 플로트 쓸때는 F를 붙여줘야함.
		double cal = 2000.345; //8
		
		
		//문자
		char c = 'A';
		
		//boolean
		boolean result = true; // false
		
	}

}
